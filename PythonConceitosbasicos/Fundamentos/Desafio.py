print("---------")
nome = input("Qual o seu nome? \nDigite aqui: ")
idade = int(input("Digite sua idade: "))
print("---------")
print(f"Olá, {nome}")
print(f"Seu nome tem {len(nome)} letras.")
print(f"Você tem {idade} anos.")
print(f"Daqui a 5 anos você terá {idade + 5} anos.")    
print("---------")


###O que o código faz:
# O código solicita ao usuário que digite seu nome e idade, e depois imprime algumas informações sobre ele.
# O código funciona da seguinte maneira:
# 1. Solicita ao usuário que digite seu nome e armazena na variável `nome`.
# 2. Solicita ao usuário que digite sua idade e armazena na variável `idade`.
# 3. Imprime uma saudação com o nome do usuário.
# 4. Imprime o tamanho do nome do usuário.
# 5. Imprime a idade do usuário.
# 6. Imprime a idade do usuário em 5 anos.
